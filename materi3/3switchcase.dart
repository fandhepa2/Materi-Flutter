void main() {
  //Switch Case => untuk pemilihan  data, tapi datanya  tidak bisa rentang
  String menu = "Home";
  switch (menu) {
//kondisi
    case "Home":
      //proses apa yang ingin dilakukan
      print("Halaman Beranda");
      //menyudahi statement
      //kondisi ke 2
      break;

    case "profile":
      //proses apa yang ingin dilakukan
      print("Halaman profile");
      //menyudahi statement
      //kondisi ke 2
      break;

    case "Transaction":
      //proses apa yang ingin dilakukan
      print("Halaman transaction");
      //menyudahi statement
      //kondisi ke 2
      break;

    default:
      //proses apa yang ingin dilakukan
      print("Halaman tidak ada");
      //menyudahi statement
      //kondisi ke 2
      break;
  }
}
